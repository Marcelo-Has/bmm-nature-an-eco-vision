    private void cadastraUsuario(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastraUsuario

        if (JOptionPane.showConfirmDialog(null, "Deseja mesmo cadastrar este usuário?", "Cadastro", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, icone) == JOptionPane.YES_OPTION) {

            if (txtCadNomeU.getText().equals("") || txtCadLoginU.getText().equals("") || String.valueOf(txtCadSenhaU.getPassword()).equals("") || txtCadCodU.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos para cadastrar um novo usuário!", "Atenção", JOptionPane.INFORMATION_MESSAGE, icone);
            } else {
                Usuario u = new Usuario();
                UsuarioDao daoU = new UsuarioDao();

                u.setNome(txtCadNomeU.getText());
                u.setLogin(txtCadLoginU.getText());
                u.setSenha(String.valueOf(txtCadSenhaU.getPassword()));
                u.setCodT(txtCadCodU.getText());
                daoU.create(u);

                txtCadNomeU.setText("");
                txtCadLoginU.setText("");
                txtCadSenhaU.setText("");
                txtCadCodU.setText("");

                leTabelaUsuario();
            }
        }
    }//GEN-LAST:event_cadastraUsuario

    private void chamaTelaUpUsuario(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chamaTelaUpUsuario

        if (TelaUpUsuario.instancia == null) {
            if (tbUsuario.getSelectedRow() != -1) {

                TelaUpUsuario upUsuario = new TelaUpUsuario();

                getParent().add(upUsuario);
                upUsuario.setVisible(true);
                upUsuario.toFront();
                upUsuario.centralizaTela();

                upUsuario.setValoresUsuario(
                        tbUsuario.getValueAt(tbUsuario.getSelectedRow(), 0).toString(),
                        tbUsuario.getValueAt(tbUsuario.getSelectedRow(), 1).toString(),
                        tbUsuario.getValueAt(tbUsuario.getSelectedRow(), 2).toString(),
                        tbUsuario.getValueAt(tbUsuario.getSelectedRow(), 3).toString(),
                        tbUsuario.getValueAt(tbUsuario.getSelectedRow(), 4).toString()
                );
                
                this.enable(false);

            } else {
                JOptionPane.showMessageDialog(null, "Selecione um usuário", "Atenção", JOptionPane.INFORMATION_MESSAGE, icone);
            }
        } else {
            TelaUpUsuario.instancia.toFront();
        }

    }//GEN-LAST:event_chamaTelaUpUsuario

    private void excluiUsuario(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluiUsuario

        if (tbUsuario.getSelectedRow() != -1) {
            if (JOptionPane.showConfirmDialog(null, "Deseja mesmo excluir este usuário?", "Exclusão", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, icone) == JOptionPane.YES_OPTION) {

                Usuario u = new Usuario();
                UsuarioDao daoU = new UsuarioDao();

                u.setId((int) tbUsuario.getValueAt(tbUsuario.getSelectedRow(), 0));
                daoU.delete(u);

                leTabelaUsuario();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um usuário", "Atenção", JOptionPane.INFORMATION_MESSAGE, icone);
        }
    }//GEN-LAST:event_excluiUsuario


