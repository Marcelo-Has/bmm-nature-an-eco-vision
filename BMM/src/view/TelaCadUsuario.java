    public void leTabelaUsuario() {
        DefaultTableModel modelo = (DefaultTableModel) tbUsuario.getModel();
        modelo.setNumRows(0);

        UsuarioDao uDao = new UsuarioDao();

        for (Usuario user : uDao.read()) {

            modelo.addRow(new Object[]{
                user.getId(),
                user.getNome(),
                user.getLogin(),
                user.getSenha(),
                user.getCodT(),
                user.getNomeT(),
                user.getUltimaModificacao()
            });

        }

    }

